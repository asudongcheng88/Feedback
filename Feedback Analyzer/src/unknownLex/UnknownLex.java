package unknownLex;

public class UnknownLex {
	
	private String unLexText;
	private int timesAppeared;
	boolean exist;
	
	public UnknownLex() {
		super();
	}

	public String getUnLexText() {
		return unLexText;
	}

	public void setUnLexText(String unLexText) {
		this.unLexText = unLexText;
	}

	public int getTimesAppeared() {
		return timesAppeared;
	}

	public void setTimesAppeared(int timesAppeared) {
		this.timesAppeared = timesAppeared;
	}

	public boolean isExist() {
		return exist;
	}

	public void setExist(boolean exist) {
		this.exist = exist;
	}
	
	
	
}
